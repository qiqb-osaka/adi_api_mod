@chip_id = ARGV[0].to_i

def read_reg(addr)
  cmd = "./udpsendrecv ad9082 r #{@chip_id} #{addr.to_s(16)} 0"
  ret = `#{cmd}`
  value = ret.split("\n")[1].strip().gsub("return value: ", "").to_i(16)
end

def at(value, index)
  return (value >> index) & 0x01
end

def rg(value, b, e)
  mask = ~(0xFFFF << (b-e))
  return (value >> e) & mask
end

def h(value)
  return format('%02X', value)
end

def r(key, addr)
  v = read_reg(addr)
  puts("#{key}(#{format('%04x',addr)}),,#{h(v)}")
  return v
end

